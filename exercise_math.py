def math():
    """
    Ejercicio 1 - Operaciones Matemáticas

    Dado dos números enteros a y b, imprimir:
    1. La suma
    2. La diferencia
    3. El producto
    4. El promedio
    5. El cociente entero
    6. El resto de la división entera
    7. El valor real de la división
    """
    a = 57
    b = 7

    print("1. La suma es:")
    print(a+b)

    print("2. La diferencia es:")
    print(a-b)

    print("3. El producto es:")
    print(a*b)

    print("4. El promedio es:")
    print((a+b) /2)

    print("5. El cociente entero es:")
    print(a//b)

    print ("6. El resto de la division entera es:")
    print(a%b)

    print("7. El valor real es:")
    print(a/b)

